  String get currentLanguage {
    String languageCode = _locale.languageCode;
    String? countryCode = _locale.countryCode;
    
    // Try with full locale code first (e.g., zh_CN)
    String fullKey = '${languageCode}_$countryCode';
    if (languageNames.containsKey(fullKey)) {
      return languageNames[fullKey]!;
    }
      
    // Try with just language code (e.g., en)
    if (languageNames.containsKey(languageCode)) {
      return languageNames[languageCode]!;
    }
    
    // If still not found, try alternative combinations
    final alternatives = <String>[
      if (countryCode != null) '${languageCode}_${countryCode.toUpperCase()}',
      '${languageCode}_${languageCode.toUpperCase()}',
    ];
    
    for (String key in alternatives) {
      if (languageNames.containsKey(key)) {
        return languageNames[key]!;
      }
    }
    
    // Fallback to a readable format if no match found
    return languageNames[languageCode] ?? 
           languageNames['${languageCode}_${countryCode ?? ''}'.trim()] ?? 
           'Unknown';
  }

  static const LocalizationsDelegate<AppLocalizations> delegate = _AppLocalizationsDelegate();
}

class _AppLocalizationsDelegate extends LocalizationsDelegate<AppLocalizations> {
  const _AppLocalizationsDelegate();

  @override
  bool isSupported(Locale locale) {
    final requestedCountry = locale.countryCode;
    for (var supportedLocale in AppLocalizations.supportedLocales) {
      if (supportedLocale.languageCode == locale.languageCode) {
        // If the requested locale has no country code, accept any variant
        // e.g. device sends Locale('zh') → match Locale('zh','CN')
        if (requestedCountry == null || requestedCountry.isEmpty) {
          return true;
        }
        // Supported locale has no country code requirement → match all
        if (supportedLocale.countryCode == null) {
          return true;
        }
        // Both have country codes — must match exactly
        if (supportedLocale.countryCode == requestedCountry) {
          return true;
        }
      }
    }
    return false;
  }

  @override
  Future<AppLocalizations> load(Locale locale) async {
    return AppLocalizations(locale);
  }

  @override
  bool shouldReload(_AppLocalizationsDelegate old) => false;
}
